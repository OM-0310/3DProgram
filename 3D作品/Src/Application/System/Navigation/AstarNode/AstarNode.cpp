#include "AstarNode.h"

// コンストラクタでノードの位置を設定し、他のメンバ変数を初期化
AstarNode::AstarNode(int _posX, int _posZ) :
	m_posX(_posX),
	m_posZ(_posZ),
	m_gCost(0),
	m_hCost(0),
	m_isObstacle(false)
{
}

//================================================================================================================================
// ノードの合計コストを返す関数・・・ここから
//================================================================================================================================
float AstarNode::fCost() const
{
	return m_gCost + m_hCost;
}
//================================================================================================================================
// ノードの合計コストを返す関数・・・ここまで
//================================================================================================================================

//================================================================================================================================
// ノードの透過性を確認するための演算子オーバーロード・・・ここから
//================================================================================================================================
bool AstarNode::operator==(const AstarNode& _other) const
{
	return m_posX == _other.m_posX && m_posZ == _other.m_posZ;
}
